#!/usr/bin/perl -w
use strict;
#-----------------------------------------------------------------------------
#----------------------------------- MAIN ------------------------------------
#-----------------------------------------------------------------------------
my $usage = " 
findmodel_input.pl <3_way_alignment_file> > find_model_input.txt\n\n";
die ($usage) unless $ARGV[0];
my $file = $ARGV[0];

my $length = 1e5;

my %seq;
open FH, $file;
while (<FH>) {
	next if /^[>\s]/;

	my @i = /(\S+)/g;
	next unless @i==2;

	$seq{$i[0]} .= $i[1];
}
close FH;

foreach my $sname (keys %seq) {
	print ">$sname\n";
	print substr $seq{$sname}, 0, $length;

	print "\n";
}

